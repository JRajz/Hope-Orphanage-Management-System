<b><i><h2><font color="#0033FF">
" To tell the child or not ? "</font></h2>
</i></b>
A major fear that plagues adoptive parents is that the child may want to seek out its biological parents when he becomes older. What if the child cannot get over his abandonment by his biological parent? Prospective adoptive parents are always apprehensive that this sense of abandonment may be so strong that the child may never be fully able to reconcile himself to the fact that they are not his parents no matter what they do. This may also lead to the child wanting to seek out his biological parents when he becomes older.<br />
Telling your child about his or her adoption is a must. You should talk to your child about it at the right time, openly and honestly.<br />
The time will come when you will have to tell your child that he or she is adopted. Take it as your responsibility rather than running away from the reality. Do not get overwhelmed; just make your child understand his or her importance in your life. Do not wait till your child gets older. Discussing this topic at the right time will make it easy for both of you to cope with it. Here are few things that will support you while telling your child about adoption.<br />
<b><i><h2><font color="#0033FF">
Do not be too late </font></h2>
</i></b><br />
Age of the child for telling him or her about adoption can also depend upon the comfort level of parents. However do not wait for a long time to tell your child. It is better to start talking to your child at the younger age. A child in the age group of 2 to 4 may not be in a position to understand what adoption is. You can start talking to your child when he or she turns 7 years old. It is the best age when child can easily become a part of this sensitive process. It helps child to face the reality in the future life easily.<br />
<b><i><h2><font color="#0033FF">
An ongoing process</font></h2>
</i></b><br />
Talking to the child about adoption should be an ongoing process. You can start this process by telling your child how happy you are to have him. How your child's entrance in the family made it complete. This will create a sense of being special for your child. Talking to your child regularly will help you to reveal the entire adoption story in pieces till he or she matures.<br />
<b><i><h2><font color="#0033FF">
Be honest</font></h2>
</i></b><br />
Be honest and open to your child while you talk about adoption. Be careful that you do not portray adoption as a weird thing to your child. Try to answer all the questions of your child. Have a positive attitude while talking. Make adoption a casual thing at your home and with relatives and friends. Never make adoption a family secret.<br />
<b><i><h2><font color="#0033FF">
Consequences of hiding truth </font></h2>
</i></b><br />
Your child might get hurt if he gets to know about adoption from others. Sometimes friends and relatives may direct the understanding of your child in a wrong way. You should not wait to tell truth till child is attaining maturity. Critical condition can arise even if the truth is withheld till the child attains the age of 16, 18 or 30. It can be difficult for you to clear the misconceptions of your child in this situation. So it is better to avoid these kinds of situations by taking early initiative.<br />
<b><i><h2><font color="#0033FF">
Prepare your child to deal with the society</font></h2>
</i></b><br />
As it is important to tell your child about adoption it is equally important to teach him or her to face the social problems. There will be some people who will be rude to your child. Teach your child how to deal with such people from the beginning. Tell your child what should be the answers when people ask about his or her adoption. You can also talk to your child's school, teachers, child's friends and their parents. This will help you to make your child feel comfortable.<br />
When you start doing this, you will realise that the love and care will not let you down. After all parenting has an upper hand over giving birth to the child. And nothing has an upper hand over love. So make your child feel that nothing is as important as his entry in your life. Your love and care will bring out good result and help you to make your child a good human being.</h3>
